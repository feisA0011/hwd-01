		<h1>Choose editor</h1>
		<p class="subtitle">
			This is a guide to finding your first and only text editor.
		</p>
		<div class="section">
			<h2>Introduction to choosing the right editor</h2>
			<p>You can find a bunch of text editors out there today, and the
				task of finding one that fits your needs may seem like an
				overwhelming task. As you try to find your way in the jungle
				of text editors, you have to consider their advantages and
				disadvantages when choosing the right one. Here are some
				important questions to consider:
			</p>
			<ul class="questions">
				<li>Do you want to pay money for the text editor?</li>
				<li>Do you have any idealistic and political concerns when
				choosing the right technology?</li>
				<li>Does the text editor have to be userfriendly with an
				easy-to-use userinterface?</li>
				<li>Does customization and personalization of the text editor
				mean a lot to you?</li>
				<li>And what about performance when working with large
				projects?
			</ul>
			<h2>The 4 popular editors</h2>
			<ul>
				<li>
					<h3>Atom</h3>
					<p>
						Atom is free and open source, so if you care about open
						source and free softwares, this is the perfect choice. It
						is developed by the <strong>Github community</strong>,
						with the members contributing to the <strong>many add-on
						packages</strong> that are all developed by the community.
						This means that you can extend Atom's capabilities and
						features with a high degree of freedom and choices
						<strong>(6.452 packages and themes)</strong>. It
						furthermore has a very userfriendly design, which is
						completely customizable. This means that you can make it
						look just the way you like it and that it is <strong>very
						easy to pick up for a beginner</strong>. An issue which
						is important to consider is the performance. Sometimes
						<strong>Atom feels slow</strong>, it consumes a lot of
						memory and tend to crash if you load large files. However
						it has gotten a lot better since it was first released,
						which is only 3 years ago. In fact, Atom is actually a
						pretty <strong>new player in the field</strong>, only
						beeing a couple of years old, however it has already
						gained huge popularity among the many developeres out
						there. You should choose Atom if you care about open
						source and you want an editor that is customizable and
						easy to pick up on for beginners.
					</p>
					<p class="link">
						You can read more
						about Atom and download the editor at
						<a href="https://atom.io/" target="_blank">www.Atom.io</a>
					</p>
				</li>
				<li>
					<h3>Visual Studio Code</h3>
					<p>
						Visual Studio Code is the attempt by Microsoft to create a
						<strong>free and lightweight, but powerfull and flexible
						text teditor</strong>. Microsoft already sits on the
						heavy Visual Studio editions, which is 3 GB+ in size and
						therefore huge and massive and not really ideal for
						development purposes. This new edition tries to do the
						same job but it is just much smaller in size. It is also
						pretty <strong>new to the market (from 2015)</strong> and
						comes with a <strong>lot of avaliable
						extensions.</strong> The userinterface is customizable,
						which means that you can <strong>personalize your editor
						with themes</strong> just as you prefer. It is said to be
						easy for beginners to pick up on, however I felt that
						Atom was much more convenient for a beginner. Some of the
						featues includes the support of <strong>30+ programming
						languages and auto complete.</strong> The best thing
						about Visual Studio Code is definentely the <strong>small
						size and thus the quick installation and usage.</strong>
						So if you don't care about big capitalistic superpowers
						keeping and selling your data, you should definentely go
						for this text editor, since it is fast and flexible!
					</p>
					<p class="link">
						You can read more about Visual Studio Code
						and download the software at
						<a href="https://code.visualstudio.com/"
						target="_blank">www.code.visualstudio.com</a>
					</p>
				</li>
				<li>
					<h3>Sublime</h3>
					<p>
						Sublime Text is propietary software and can be purchased
						for <strong>$70 with free trial.</strong> It is developed
						by former Google Engineer Jon Skinner and was first
						released in 2007, which means that it had time to mature
						for more than 10 years. It is said to be the third most
						popular development environment according to a survey
						made by Stackoverflow in 2016. It is <strong>small in
						size</strong>, as the installation archieve is only 8 MB
						and the program only use 22 MB of RAM. Sublime is
						therefore fast and lightweight, but still powerfull. It
						has tons of plugins and features avaliable, however the
						<strong>quality of the plugins is questionable</strong>,
						and sometimes the loading time can be high. Users agree
						that it has a <strong>clean and simple interface</strong>
						with great code highlighting and completion, but it is
						<strong>not always beginner friendly</strong>. Several
						users agree that it is too complicated to pick up on, not
						only for beginners, but also for experienced developers.
						You should choose Sublime Text if you are interested in
						an extensive variety of plugins and features and a fast
						and powerfull tool. However you should <strong>watch out
						for the quality and consequences of the many
						extensions</strong>, that tend to slow the
						performance.
					</p>
					<p class="link">
						You can read more about Sublime Text and
						download the software at
						<a href="https://www.sublimetext.com/"
						target="_blank">www.sublimetext.com</a>
					</p>
				</li>
				<li>
					<h3>Notepad++</h3>
					<p>
						Notepad++ is <strong>free and open source
						software</strong> with a GPL license, and is a familiar
						name to many. It runs in the Microsoft environment and is
						basically an enhanced version of their native program
						Notepad. It has a <strong>small program size and high
						execution speed</strong>, and Notepad++ is actually
						trying to <strong>reducing global carbon
						emissions</strong> by optimizing as many routines as
						possible by using less CPU power, thereby reducing power
						consumption. Notepad++ supports tons of programming
						languages but it is most <strong>popular for writing
						HTML, CSS and JavaScript.</strong> Some features of the
						tool
						include <strong>color coding, language dependent
						formatting and autocomplete.</strong> Its functionality
						can be extended by hundreds of plugins, and as it is very
						fast and lightweight, it has quick load times and can
						manage large files without lags or
						crashes. However, some users report that it will crash
						when trying to handle large files over 200MBs. A pitfall
						of the ligthweight software is, that it <strong>does not
						offer any themes or customizable interface</strong> like
						all the other editors support. With its simplicity the
						userinterface kind of <strong>looks like a Windows 95
						program</strong>, which reduces the userexperience and
						userfriendlieness. You should choose Notepad++ if you
						care about free and open source and furthermore would
						like to reduce your carbon footprints, however you should
						not care too much about userexperience and the
						userinterface.
					</p>
					<p class="link">
						You can read more abut Notepad++ and
						download the software at
						<a href="https://notepad-plus-plus.org/"
						target="_blank">www.notepad-plus-plus.org</a>
					</p>
				</li>
			</ul>
