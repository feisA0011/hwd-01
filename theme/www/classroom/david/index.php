<h1>Basic semantic HTML</h1>

<p>In this article, you will get to work with <em>h1</em>, <em>h2</em>, <em>h3</em>, <em>p</em>, <em>strong</em> and <em>em</em> tags.</p>

<h2>The basic semantic text tags</h2>

<p>The minimum set of semantic text HTML tags contains <em>h1</em>, <em>h2</em>, <em>p</em>, <em>strong</em> and <em>em</em>. Although there are many other 
semantic html tags, <strong>these are the most important ones to learn</strong>.</p>

<p>Other semantic HTML tags include <em>table</em>, <em>ul</em>, <em>ol</em> and <em>dl</em>.</p>

<p>We will look more at the other semantic tags later.</p>


