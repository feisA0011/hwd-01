Others - Research and choose your code editor:
Your text editor is what you use to write code and as such, it is probably the most important tool for a developer.
A text editor is not just a text editor – though they all kinda do the same, they all have different features, strengths and weaknesses. You don't want to shift to a new one every week, as you'd never get the full benefits of really knowing your tool. That is why you should choose your editor with some consideration and stick with it for a while.

Aside from personal preference, there really is only one strict requirement for your text editor: It needs to support Unix line-endings. As a very clear recommendation, it should NOT have WYSIWYG editing capabilities.

Below you will find some links, where you can read more about the most popular options. Do some research and pick one that speaks to you in one way or another.
After you have picked your editor, spend some time to get to know it. Look through the keyboard shortcut list and learn at least 5 cool shortcuts. Find a nice theme (for syntax color coding). Learn how to open a project (a folder with many files).

