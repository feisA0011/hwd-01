*First look at the Firefox Developer Tools:*
Open Firefox and go to http://dr.dk
Press CTRL+SHIFT+c on Windows, or CMD+ALT+c on Mac
You'll see the Firefox developer tools open in the bottom of Firefox.

The default tab is the *inspector* tab
On the left side you'll see the HTML of the page you are visiting.
On the right side you'll see the CSS of the page you are visiting.

To the left of Inspector tab, there is a small square icon with an arrow on it. If you click on it, it becomes blue.
When it is blue, try moving the mouse around in the page above. You'll see the HTML related to where your mouse is being highligthed in the HTML view of the Inspector - and the CSS of that element in the CSS view.

Another important tab is the *network* tab
The network tab allows you to see which ressources are being loaded to show this page. It also shows how many KB each ressourse is and how long it takes to load.
Try to refresh the browser, while you keep the network tab open.


