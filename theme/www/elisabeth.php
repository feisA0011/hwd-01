	<h2 class="overskrift2">Kale pizza with veggies</h2>
	<ul class="about">
		<li>10 slices of pizza</li>
		<li>Preparation time: 45 minutes</li>
		<li>Total time: 1 hour 15 minutes</li>
	</ul>
	<h3>Ingredients</h3>
	<h4>The Crust</h4>
	<ul class="ingredients">
		<li>300 grams of organic sweetheart cabbage</li>
		<li>120 grams of organic kale</li>
		<li>100 grams of organic white onion</li>
		<li>3 cloves of organic garlic</li>
		<li>2 organic, free range eggs</li>
		<li>1 tablespoon of organic quinoa</li>
		<li>1 tablespoon of organic tahini</li>
		<li>1 tablespoon of corianderseeds</li>
		<li>1 tablespoon of salt</li>
		<li>Some lemon juice</li>
	</ul>
	<h4>The Sauce</h4>
	<ul class="ingredients">
		<li>400 grams of organic tomatoes</li>
		<li>Mixed spices of your own preference</li>
		<li>1 Oranic white onion</li>
	</ul>
	<h4>The Topping</h4>
	<ul class="ingredients">
		<li>125 grams of organic mozarella cheese</li>
		<li>2 organic carrots</li>
		<li>75 grams of organic bell pepper</li>
		<li>3 organic portobello mushrooms</li>
	</ul>
	<h3>Procedure</h3>
	<ol class="procedure">
		<li>Chop the cabbage, kale, onion and garlic in a mini chopper. Add in the quinoa, tahini, spices and lemon juice. Stir well. Mix the mass with the eggs.</li>
		<li>Spread the mass in a thin layer on a baking pan.</li>
		<li>Bake the crust for 20 minutes.</li>
		<li>Mix the ingredients for the sauce in a hot pan.</li>
		<li>When the crust is hard and attains a golden glow, take it out and spread out he sauce.</li>
		<li>Cut the cheese and veggies in thin slices and add it all to the pizza.</li>
		<li>Bake the pizza for 15 minutes.</li>
		<li>When the cheese is melting and the pizza looks yummie take it out and dinner is ready to be served!</li>
	</ol>
